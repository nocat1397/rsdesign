    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $input = $request->all();
        
        
        
        if ($file = $request->file('file')) {
            $name = $file->getClientOriginalName();
            $file->move('images',$name);
            $input['file'] = $name;
        }
        $images=Image::create($input);
        $project_id=Project::find($request->id);
        $cat_id=Category::find($request->category_id);
        

        $items=Image::find($images->id);
        
        $cat_id->images()->attach($items);
        
        $project_id->images()->attach($items);
        
        return "success";
    }

